package com.upskill.java;

public class AccessModifier {

		// 4 (four) Different Accesss Modifiers in Java :
	
		public String name = "Rushdi";         // public = any class can access
		private int age = 55;                  // private =  only same class can access
		protected int ssn = 123456789;         // protected = classes in the same package & sub class
        String address = "Heritage Tower";     // default = classes in same package 
				
		public static void main(String[] args) {
	}

}
